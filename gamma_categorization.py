#Design Patterns are typically divided into three categories:

#The name come after Eric Gamma, who wrote the book "Design Patterns: Elements of Reusable Object-Oriented Software".

#1. Creational Patterns 
# - Deal with the creation (construction) of objects
#     You might think creation of an object is quite simply invoking the constructor, but things are quite often a bit more complicated 
# - Explicit (Constructor) vs implicit (DI, Reflection, etc).
# - Wholesales creation of objects vs peicewise(step by step) creation of objects.
#
#2. Structural Patterns
# - Mainly concerned with the structure of classes and objects.
# - Many patterns are wrappers that mimic the underlying class interface.
# - Stress the importatnce of API design.

# 3. Behavioral Patterns
# - They are all different, doing their own thing., no central theme.
